def get_locale():
    """Detect locale from Accept-Language header."""
    lang = request.headers.get('Accept-Language', 'pt')
    if lang.startswith('en'):
        return 'en'
    return 'pt'

def t(key, **kwargs):
    """Translate a key based on the current locale."""
    locale = get_locale()
    message = TRANSLATIONS.get(locale, TRANSLATIONS['pt']).get(key, key)
    
    if kwargs:
        return message.format(**kwargs)
    return message
